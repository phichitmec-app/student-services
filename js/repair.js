let cachedRepairHistory = null;

const repairMenuData = [
    {
        menuName: "แจ้งซ่อม",
        consolidatedView: true,
        groups: [
            {
                groupName: "ข้อมูลผู้แจ้ง",
                fields: [
                    { index: 1, label: "ชื่อผู้แจ้งซ่อม", type: "text", readonly: true, width: "half" }, // B
                    { index: 2, label: "เบอร์ติดต่อ", type: "text", width: "half" }, // C
                    { index: 3, label: "อีเมล์", type: "email", fullWidth: true }, // D
                    { index: 4, label: "หมายเลขห้อง", type: "text", readonly: true, fullWidth: true }, // E
                ]
            },
            {
                groupName: "รายละเอียดการแจ้งซ่อม",
                fields: [
                    { index: 5, label: "รายละเอียดชำรุด", type: "textarea", required: true, fullWidth: true }, // F
                    { index: 7, label: "แนบรูปภาพ (ถ้ามี)", type: "file", accept: "image/*", fullWidth: true }, // H
                ]
            }
        ]
    }
];

function renderRepairForm() {
    const container = document.getElementById('repairFormContainer');
    if (!container) return;
    container.innerHTML = '';

    const menu = repairMenuData[0];

    const consolidatedContent = document.createElement('div');
    consolidatedContent.className = 'space-y-6';

    menu.groups.forEach(group => {
        // Reusing createGroupElement from studentdata.js
        // Note: createGroupElement needs to be accessible globally or imported.
        // Assuming it's global as per current project structure.
        const groupDiv = createGroupElement(group); 
        consolidatedContent.appendChild(groupDiv);
    });

    const actionDiv = document.createElement('div');
    actionDiv.className = 'mt-8 flex justify-end border-t pt-6';
    const saveRepairBtn = document.createElement('button');
    saveRepairBtn.type = 'button';
    saveRepairBtn.className = 'bg-blue-600 hover:bg-blue-700 text-white font-bold py-3 px-8 rounded-lg shadow-lg transition duration-300 flex items-center gap-2 text-base';
    saveRepairBtn.innerHTML = `
        <span>ส่งคำแจ้งซ่อม</span>
        <svg class="animate-spin h-5 w-5 text-white hidden" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
            <circle class="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" stroke-width="4"></circle>
            <path class="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
        </svg>
    `;
    saveRepairBtn.onclick = (e) => submitRepairRequest(e);
    actionDiv.appendChild(saveRepairBtn);
    consolidatedContent.appendChild(actionDiv);

    container.appendChild(consolidatedContent);

    // Populate initial data
    populateRepairData();

    // Load repair history
    loadRepairHistory();
}

function populateRepairData() {
    // currentRowData is from login.js, contains student's fill_data
    // Column B = ชื่อผู้แจ้งซ่อม (ชื่อนิสิต + สกุลนิสิต) -> currentRowData[5] + currentRowData[6]
    // Column C = เบอร์ติดต่อ (เบอร์โทรศัพท์นิสิต) -> currentRowData[14]
    // Column D = email (อีเมล์ส่วนตัวนิสิต) -> currentRowData[16]
    // Column E = หมายเลขห้อง (จาก fill_data column CH, which is index 85) -> currentRowData[85]

    const studentName = currentRowData[5] + ' ' + currentRowData[6]; // F + G
    const studentPhone = currentRowData[14]; // O
    const studentEmail = currentRowData[16]; // Q
    const roomNumber = currentRowData[85]; // CH (index 85)

    // Populate the form fields
    const nameInput = document.getElementById('input_1');
    if (nameInput) nameInput.value = studentName;

    const phoneInput = document.getElementById('input_2');
    if (phoneInput) phoneInput.value = studentPhone;

    const emailInput = document.getElementById('input_3');
    if (emailInput) emailInput.value = studentEmail;

    const roomInput = document.getElementById('input_4');
    if (roomInput) roomInput.value = roomNumber;
}

async function submitRepairRequest(e) {
    if (e) e.preventDefault();

    const btn = e.currentTarget;
    btn.disabled = true;
    btn.classList.add('opacity-75', 'cursor-not-allowed');
    const btnSpan = btn.querySelector('span');
    const btnSvg = btn.querySelector('svg');
    if(btnSpan) btnSpan.classList.add('hidden');
    if(btnSvg) btnSvg.classList.remove('hidden');

    const statusText = document.getElementById('uploadStatus');

    try {
        let repairData = [];
        // A = primary key (generated by backend)
        // B = ชื่อผู้แจ้งซ่อม (input_1)
        // C = เบอร์ติดต่อ (input_2)
        // D = email (input_3)
        // E = หมายเลขห้อง (input_4)
        // F = รายละเอียดชำรุด (input_5)
        // G = (empty for now)
        // H = แนบรูป (input_7)
        // I = (timestamp) (generated by backend)

        repairData[1] = document.getElementById('input_1').value; // ชื่อผู้แจ้งซ่อม
        repairData[2] = document.getElementById('input_2').value; // เบอร์ติดต่อ
        repairData[3] = document.getElementById('input_3').value; // อีเมล์
        repairData[4] = document.getElementById('input_4').value; // หมายเลขห้อง
        repairData[5] = document.getElementById('input_5').value; // รายละเอียดชำรุด

        // Validate required fields
        if (!repairData[1] || !repairData[4] || !repairData[5]) {
            Swal.fire({ icon: 'warning', title: 'แจ้งเตือน', text: 'กรุณากรอกข้อมูลให้ครบถ้วน', confirmButtonColor: '#1e3a8a' });
            return;
        }

        const fileInput = document.getElementById('input_7');
        let imageUrl = '';

        if (fileInput && fileInput.files.length > 0) {
            if (statusText) {
                statusText.classList.remove('hidden');
                statusText.innerHTML = `
                    <div class="flex flex-col items-center w-full max-w-sm mt-2">
                        <span class="text-sm text-slate-600 mb-1 font-medium" id="repairPercentText">กำลังเตรียมรูปภาพ... 0%</span>
                        <div class="w-full bg-slate-200 rounded-full h-2.5 shadow-inner">
                            <div id="repairProgressBar" class="bg-blue-600 h-2.5 rounded-full transition-all duration-300" style="width: 0%"></div>
                        </div>
                    </div>
                `;
            }
            // uploadSingleFile is from studentdata.js, assuming it's global.
            // Pass 'repair' to indicate the folder for repair images.
            const uploadResult = await uploadSingleFile(fileInput, 'repair', (percent) => {
                const pText = document.getElementById('repairPercentText');
                const pBar = document.getElementById('repairProgressBar');
                if (pText) pText.innerText = `กำลังอัปโหลด... ${Math.round(percent)}%`;
                if (pBar) pBar.style.width = `${percent}%`;
            }); 
            if (uploadResult.status === 'success') {
                imageUrl = uploadResult.url;
            } else {
                throw new Error("อัปโหลดรูปภาพไม่สำเร็จ: " + (uploadResult.message || "ไม่ทราบสาเหตุ"));
            }
        }
        repairData[7] = imageUrl; // H

        if (statusText) {
            statusText.innerHTML = "กำลังส่งคำแจ้งซ่อม... <br><span class='text-xs text-blue-500'>(ระบบกำลังจัดคิวหากมีผู้ใช้งานพร้อมกัน)</span>";
            statusText.classList.remove('hidden');
        }

        const response = await fetch(GAS_URL, {
            method: 'POST',
            body: JSON.stringify({ action: 'submitRepair', data: repairData })
        });

        const textResponse = await response.text();
        let result;
        try {
            result = JSON.parse(textResponse);
        } catch (e) {
            console.error("Server HTML Error:", textResponse);
            throw new Error("การเชื่อมต่อ Apps Script ผิดพลาด (กรุณา Deploy เป็น New Version และสิทธิ์ Anyone)");
        }

        if (result.status === 'success') {
            Swal.fire({
                icon: 'success',
                title: 'ส่งคำแจ้งซ่อมสำเร็จ!',
                text: 'คำแจ้งซ่อมของคุณถูกส่งไปยังระบบเรียบร้อยแล้ว',
                confirmButtonColor: '#1e3a8a'
            });
            // Clear form after successful submission
            document.getElementById('input_5').value = ''; // Clear description
            if (fileInput) fileInput.value = ''; // Clear file input
            const linkDiv = document.getElementById('link_7');
            if (linkDiv) linkDiv.classList.add('hidden');
            
            // โหลดข้อมูลพื้นฐานกลับมาใหม่
            populateRepairData();

            // โหลดประวัติการแจ้งซ่อมใหม่มาแสดง
            cachedRepairHistory = null;
            loadRepairHistory();
        } else {
            Swal.fire({ icon: 'error', title: 'เกิดข้อผิดพลาด', text: result.message, confirmButtonColor: '#1e3a8a' });
        }
    } catch (err) {
        Swal.fire({ icon: 'error', title: 'ส่งคำแจ้งซ่อมไม่สำเร็จ', text: err.message, confirmButtonColor: '#1e3a8a' });
    } finally {
        btn.disabled = false;
        btn.classList.remove('opacity-75', 'cursor-not-allowed');
        if(btnSpan) btnSpan.classList.remove('hidden');
        if(btnSvg) btnSvg.classList.add('hidden');

        if (statusText) {
            statusText.classList.add('hidden');
            statusText.innerHTML = "";
        }
    }
}

async function loadRepairHistory(forceRefresh = false) {
    const loading = document.getElementById('repairHistoryLoading');
    const table = document.getElementById('repairHistoryTable');
    const tbody = document.getElementById('repairHistoryBody');
    const emptyMsg = document.getElementById('repairHistoryEmpty');

    if(!loading) return;

    const renderTable = (data) => {
        tbody.innerHTML = '';
        if (data && data.length > 0) {
            data.forEach(row => {
                const dateStr = new Date(row[0]).toLocaleString('th-TH');
                const details = row[1] || '-';
                const image = row[2] ? `<a href="${row[2]}" target="_blank" class="text-blue-500 hover:text-blue-700 underline">ดูรูปภาพ</a>` : '-';
                const status = row[3] || 'รอดำเนินการ'; // ถ้า J ว่าง ให้แสดงว่า รอดำเนินการ

                const tr = document.createElement('tr');
                const statusClass = status === 'รอดำเนินการ' ? 'bg-yellow-100 text-yellow-800' : (status === 'ยกเลิก' ? 'bg-red-100 text-red-800' : 'bg-green-100 text-green-800');
                tr.innerHTML = `
                    <td class="py-3 px-4 text-left whitespace-nowrap">${dateStr}</td>
                    <td class="py-3 px-4 text-left break-words">${details}</td>
                    <td class="py-3 px-4 text-center">${image}</td>
                    <td class="py-3 px-4 text-center">
                        <span class="px-2 py-1 rounded-full text-xs font-semibold ${statusClass}">${status}</span>
                    </td>
                `;
                tbody.appendChild(tr);
            });
            table.classList.remove('hidden');
            emptyMsg.classList.add('hidden');
        } else {
            table.classList.add('hidden');
            emptyMsg.classList.remove('hidden');
        }
    };

    if (!forceRefresh && cachedRepairHistory !== null) {
        renderTable(cachedRepairHistory);
        loading.classList.add('hidden');
        return;
    }

    loading.classList.remove('hidden');
    table.classList.add('hidden');
    emptyMsg.classList.add('hidden');

    const studentName = currentRowData[5] + ' ' + currentRowData[6]; // ชื่อ-สกุล
    const roomNumber = currentRowData[85]; // หมายเลขห้อง

    try {
        const response = await fetch(GAS_URL, {
            method: 'POST',
            body: JSON.stringify({
                action: 'getRepairHistory',
                studentName: studentName,
                roomNumber: roomNumber
            })
        });

        const textResponse = await response.text();
        let result;
        try {
            result = JSON.parse(textResponse);
        } catch (e) {
            console.error("Server HTML Error:", textResponse);
            throw new Error("เซิร์ฟเวอร์ส่งกลับมาเป็น HTML (โปรดตรวจสอบการ Deploy Web App ใหม่)");
        }

        if (result.status === 'success') {
            cachedRepairHistory = result.data;
            renderTable(result.data);
        } else {
            throw new Error(result.message);
        }
    } catch (err) {
        console.error("Error loading repair history:", err);
        emptyMsg.innerText = 'เกิดข้อผิดพลาดในการโหลดประวัติการแจ้งซ่อม';
        emptyMsg.classList.remove('hidden');
    } finally {
        loading.classList.add('hidden');
    }
}